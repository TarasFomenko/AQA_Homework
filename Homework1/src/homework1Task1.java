public class homework1Task1 {
    public static void main(String[] args) {

        int number1 = 10;
        int number2 = 15;
        double plusOperationResult = number1 + number2;
        System.out.println("Результат сложения:");
        System.out.println(plusOperationResult);

        double minusOperationResult = number1 - number2;
        System.out.println("Результат вычитания:");
        System.out.println(minusOperationResult);

        double multiplyOperationResult = number1 * number2;
        System.out.println("Результат умножения:");
        System.out.println(multiplyOperationResult);

        float divideOperationResult = (float) number1 / number2;
        System.out.println("Результат деления:");
        System.out.println(divideOperationResult);
    }
}
